#!/usr/bin/env python3
"""
Prepare data files for Progressive Transformers training.

Pipeline:
  1. Select 50 joints from 75-joint MediaPipe Holistic poses
  2. Lift 2D → 3D via gopeith/3DposeEstimator
  3. Normalize, generate .text/.skels/.files + vocabulary

Reads:
  - data/usl-suspilne/annotations.csv
  - data/usl-suspilne/poses/mediapipe_holistic/{videoId}/{clipIdx}.npy  (T, 225)

Writes:
  - data/usl-suspilne/poses/mediapipe_3d/{videoId}/{clipIdx}.npy
  - experiments/progressive_transformers/model/Data/usl/{split}.text/.skels/.files
  - experiments/progressive_transformers/model/Configs/usl_src_vocab.txt

Usage:
    python experiments/progressive_transformers/prepare_data.py
    python experiments/progressive_transformers/prepare_data.py --bpe 2000
"""

import argparse
import csv
import sys
import tempfile
from collections import Counter
from pathlib import Path

import numpy as np

EXPERIMENT_DIR = Path(__file__).resolve().parent
PROJECT_ROOT = EXPERIMENT_DIR.parent.parent
SP_MODEL_PATH = EXPERIMENT_DIR / "model/Configs/usl_sp.model"

sys.path.insert(0, str(PROJECT_ROOT / "scripts"))

# Train/dev/test splits are defined by per-split CSV files
# generated by build_annotations_from_firebase.py:
#   data/usl-suspilne/{train,dev,test}.csv



def build_bpe_tokenizer(train_texts, vocab_size):
    """Train a SentencePiece BPE model on training texts, return the processor."""
    import sentencepiece as spm

    with tempfile.NamedTemporaryFile(mode="w", suffix=".txt", delete=False) as f:
        for t in train_texts:
            f.write(t + "\n")
        train_file = f.name

    SP_MODEL_PATH.parent.mkdir(parents=True, exist_ok=True)
    prefix = str(SP_MODEL_PATH).removesuffix(".model")

    spm.SentencePieceTrainer.train(
        input=train_file,
        model_prefix=prefix,
        vocab_size=vocab_size,
        model_type="bpe",
        character_coverage=1.0,
        pad_id=3,
    )
    Path(train_file).unlink()

    sp = spm.SentencePieceProcessor(model_file=str(SP_MODEL_PATH))
    print(f"Trained BPE model: vocab_size={sp.get_piece_size()}, saved to {SP_MODEL_PATH}")
    return sp


# 75-joint Holistic → 50-joint layout for Progressive Transformers:
#   Body: Nose(0), Neck(midpoint 11,12), RShoulder(12), RElbow(14), RWrist(16),
#         LShoulder(11), LElbow(13), LWrist(15)
#   Left hand: joints 33-53 (21 landmarks)
#   Right hand: joints 54-74 (21 landmarks)
# Output: 50 joints × (x, y, confidence) = 150 values
_BODY8_HOLISTIC_INDICES = [0, None, 12, 14, 16, 11, 13, 15]


def holistic_to_50joint(poses_225):
    """Convert (T, 225) Holistic poses to (T, 150) 50-joint format.

    Selects 8 upper-body joints + both hands from the 75-joint layout.
    """
    T = poses_225.shape[0]
    joints_75x3 = poses_225.reshape(T, 75, 3)

    out = np.zeros((T, 50, 3), dtype=np.float32)

    # Body (8 joints)
    for i, mp_idx in enumerate(_BODY8_HOLISTIC_INDICES):
        if mp_idx is None:  # Neck = midpoint(LShoulder=11, RShoulder=12)
            out[:, i] = (joints_75x3[:, 11] + joints_75x3[:, 12]) / 2
        else:
            out[:, i] = joints_75x3[:, mp_idx]

    # Left hand (joints 33-53 → slots 8-28)
    out[:, 8:29] = joints_75x3[:, 33:54]

    # Right hand (joints 54-74 → slots 29-49)
    out[:, 29:50] = joints_75x3[:, 54:75]

    return out.reshape(T, 150)


def select_and_lift(holistic_dir, poses_3d_dir):
    """Select 50 joints from Holistic output and lift to 3D.

    Args:
        holistic_dir: Directory with 75-joint Holistic npy files.
        poses_3d_dir: Output directory for 3D-lifted 50-joint npy files.

    Returns:
        dict with 'total', 'failed', 'cached' counts.
    """
    from lift_to_3d import lift_clip, get_skeletal_model_structure

    holistic_dir, poses_3d_dir = Path(holistic_dir), Path(poses_3d_dir)
    clips = sorted(holistic_dir.glob("*/*.npy"))
    print(f"Found {len(clips)} Holistic pose files in {holistic_dir}\n")

    if not clips:
        print("No Holistic poses found. Run extract_poses.py first.")
        sys.exit(1)

    poses_3d_dir.mkdir(parents=True, exist_ok=True)
    structure = get_skeletal_model_structure()

    failed = 0
    cached = 0
    for i, clip_path in enumerate(clips):
        video_id = clip_path.parent.name
        clip_name = clip_path.stem
        out_path = poses_3d_dir / video_id / f"{clip_name}.npy"

        if out_path.exists():
            cached += 1
            if (i + 1) % 500 == 0:
                print(f"  [{i+1}/{len(clips)}] {cached} cached so far")
            continue

        poses_225 = np.load(clip_path)
        if poses_225.shape[0] < 2 or poses_225.shape[1] != 225:
            print(f"  [{i+1}/{len(clips)}] {video_id}/{clip_name}: "
                  f"unexpected shape {poses_225.shape}, skipping")
            failed += 1
            continue

        # 75-joint → 50-joint
        poses_2d = holistic_to_50joint(poses_225)

        # 2D → 3D
        print(f"  [{i+1}/{len(clips)}] {video_id}/{clip_name}: "
              f"{poses_2d.shape[0]} frames, lifting to 3D ...")
        poses_3d = lift_clip(poses_2d, structure)

        out_path.parent.mkdir(parents=True, exist_ok=True)
        np.save(out_path, poses_3d)

    total = len(list(poses_3d_dir.glob("*/*.npy")))
    print(f"\nDone. {total} 3D poses ({cached} cached, {failed} failed)")
    return {"total": total, "failed": failed, "cached": cached}


def load_annotations(annotation_path):
    """Load annotations from usl-suspilne.csv."""
    rows = []
    with open(annotation_path, newline="", encoding="utf-8") as f:
        reader = csv.DictReader(f, delimiter="|")
        for row in reader:
            rows.append(row)
    return rows



def format_skels_line(poses_3d: np.ndarray) -> str:
    """Convert a (T, 150) 3D pose array to a single-line skeleton string.

    Each frame becomes 151 values: 150 joint coordinates + 1 counter.
    Counter = i / (N-1) for frame i of N total frames.
    """
    T = poses_3d.shape[0]
    parts = []
    for i in range(T):
        frame_vals = poses_3d[i].tolist()
        # Append counter
        counter = i / max(T - 1, 1)
        frame_vals.append(counter)
        parts.extend(frame_vals)
    return " ".join(f"{v:.6f}" for v in parts)


def prepare_all(split_dir, poses_dir, out_dir, vocab_path, norm_stats_path, bpe=None):
    """Prepare Progressive Transformers training data.

    Args:
        split_dir: Directory containing {train,dev,test}.csv split files.
        poses_dir: Directory with 3D pose npy files.
        out_dir: Output directory for .text/.skels/.files.
        vocab_path: Output path for vocabulary file.
        norm_stats_path: Output path for normalization stats.
        bpe: If set, use BPE subword tokenization with this vocab size.
    """
    split_dir, poses_dir = Path(split_dir), Path(poses_dir)
    out_dir, vocab_path = Path(out_dir), Path(vocab_path)
    norm_stats_path = Path(norm_stats_path)

    # Pass 1: load raw poses and texts from per-split CSVs
    splits = {"train": [], "dev": [], "test": []}
    skipped = 0

    for split_name in splits:
        split_csv = split_dir / f"{split_name}.csv"
        if not split_csv.exists():
            print(f"  WARNING: {split_csv} not found, skipping {split_name}")
            continue
        rows = load_annotations(split_csv)
        print(f"Loaded {len(rows)} annotations from {split_csv.name}")

        for row in rows:
            name = row["name"]
            video_id = name.split("/")[0]
            clip_idx = name.split("/")[1]

            pose_path = poses_dir / video_id / f"{clip_idx}.npy"
            if not pose_path.exists():
                skipped += 1
                continue

            poses_3d = np.load(pose_path)
            if poses_3d.shape[0] == 0 or poses_3d.shape[1] != 150:
                print(f"  WARNING: {name} has unexpected shape {poses_3d.shape}, "
                      f"skipping", file=sys.stderr)
                skipped += 1
                continue

            text = row["text_norm"]
            splits[split_name].append({
                "name": name,
                "text": text,
                "poses": poses_3d,
            })

    if skipped > 0:
        print(f"Skipped {skipped} clips (missing 3D poses)")

    for split_name, items in splits.items():
        print(f"  {split_name}: {len(items)} sequences")

    # Pass 2: compute normalization stats from training set
    train_frames = np.concatenate([item["poses"] for item in splits["train"]], axis=0)
    mean = train_frames.mean(axis=0)  # (150,)
    std = train_frames.std(axis=0)    # (150,)
    std[std < 1e-6] = 1.0  # avoid division by zero for constant joints

    norm_stats_path.parent.mkdir(parents=True, exist_ok=True)
    np.savez(norm_stats_path, mean=mean, std=std)
    print(f"\nPose normalization stats (from {train_frames.shape[0]} train frames):")
    print(f"  mean range: [{mean.min():.4f}, {mean.max():.4f}]")
    print(f"  std range:  [{std.min():.4f}, {std.max():.4f}]")
    print(f"  Saved to {norm_stats_path}")

    # Pass 3: normalize poses and format skels
    for split_items in splits.values():
        for item in split_items:
            item["poses"] = (item["poses"] - mean) / std
            item["skels"] = format_skels_line(item["poses"])
            del item["poses"]

    # BPE tokenization (optional)
    sp = None
    if bpe:
        train_texts = [item["text"] for item in splits["train"]]
        sp = build_bpe_tokenizer(train_texts, bpe)
        for split_items in splits.values():
            for item in split_items:
                pieces = sp.encode(item["text"], out_type=str)
                item["text"] = " ".join(pieces)

    # Write output files
    out_dir.mkdir(parents=True, exist_ok=True)

    for split_name, items in splits.items():
        prefix = out_dir / split_name

        with open(str(prefix) + ".text", "w", encoding="utf-8") as ft, \
             open(str(prefix) + ".skels", "w", encoding="utf-8") as fs, \
             open(str(prefix) + ".files", "w", encoding="utf-8") as ff:

            for item in items:
                ft.write(item["text"] + "\n")
                fs.write(item["skels"] + "\n")
                ff.write(item["name"] + "\n")

    # Build vocabulary
    vocab_path.parent.mkdir(parents=True, exist_ok=True)

    if sp:
        # Write SentencePiece vocab (excluding special tokens)
        with open(vocab_path, "w", encoding="utf-8") as f:
            for i in range(4, sp.get_piece_size()):  # skip <unk>, <s>, </s>, <pad>
                f.write(sp.id_to_piece(i) + "\n")
        vocab_size = sp.get_piece_size() - 4
    else:
        # Word-level vocab from training data
        train_tokens = []
        for item in splits["train"]:
            train_tokens.extend(item["text"].split())

        counter = Counter(train_tokens)
        vocab_tokens = sorted(counter.keys(), key=lambda t: (-counter[t], t))

        with open(vocab_path, "w", encoding="utf-8") as f:
            for token in vocab_tokens:
                f.write(token + "\n")
        vocab_size = len(vocab_tokens)

    print(f"\nWrote data files to {out_dir}")
    print(f"Wrote vocabulary ({vocab_size} tokens) to {vocab_path}")

    # Verification
    for split_name in ["train", "dev", "test"]:
        prefix = out_dir / split_name
        for ext in [".text", ".skels", ".files"]:
            path = str(prefix) + ext
            with open(path) as f:
                n = sum(1 for _ in f)
            print(f"  {split_name}{ext}: {n} lines")


def main():
    default_split_dir = PROJECT_ROOT / "data/usl-suspilne"
    default_holistic = PROJECT_ROOT / "data/usl-suspilne/poses/mediapipe_holistic"
    default_poses_3d = PROJECT_ROOT / "data/usl-suspilne/poses/mediapipe_3d"
    default_out = EXPERIMENT_DIR / "model/Data/usl"
    default_vocab = EXPERIMENT_DIR / "model/Configs/usl_src_vocab.txt"
    default_norm = EXPERIMENT_DIR / "model/Configs/usl_pose_norm.npz"

    parser = argparse.ArgumentParser(description="Prepare Progressive Transformers data")
    parser.add_argument("--split-dir", type=Path, default=default_split_dir,
                        help=f"Directory with train/dev/test.csv (default: {default_split_dir})")
    parser.add_argument("--holistic", type=Path, default=default_holistic,
                        help=f"Holistic poses directory (default: {default_holistic})")
    parser.add_argument("--poses-3d", type=Path, default=default_poses_3d,
                        help=f"3D poses output directory (default: {default_poses_3d})")
    parser.add_argument("--out", type=Path, default=default_out,
                        help=f"Output directory (default: {default_out})")
    parser.add_argument("--bpe", type=int, default=None,
                        help="Use BPE subword tokenization with this vocab size (e.g. 2000)")
    parser.add_argument("--skip-lift", action="store_true",
                        help="Skip 3D lifting (use existing poses_3d)")
    args = parser.parse_args()

    # Step 1: Select 50 joints and lift to 3D
    if not args.skip_lift:
        print("=== Step 1: Select joints & lift to 3D ===\n")
        select_and_lift(holistic_dir=args.holistic, poses_3d_dir=args.poses_3d)
        print()

    # Step 2: Generate training files
    print("=== Step 2: Generate training data ===\n")
    prepare_all(split_dir=args.split_dir, poses_dir=args.poses_3d,
                out_dir=args.out, vocab_path=default_vocab,
                norm_stats_path=default_norm, bpe=args.bpe)


if __name__ == "__main__":
    main()
